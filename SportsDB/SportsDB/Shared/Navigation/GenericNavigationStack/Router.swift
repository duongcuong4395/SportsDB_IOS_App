import SwiftUI

// MARK: - Router Protocol
protocol Router: ObservableObject {
    associatedtype Route: Hashable
    var path: NavigationPath { get set }
    
    func push(_ route: Route)
    func pop()
    func popToRoot()
    func replace(with route: Route)
}
